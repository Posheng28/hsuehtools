// lib/disposal/noticeLevel.ts
// 將官方「注意交易資訊」的說明文字判定為處置計數用的等級。
//
// 依 FL007225 第6條第1項第2款，處置計數之「任一款」= 第一款至第八款。
// 款九~款十四（例如第十三款＝當日沖銷成交量比率）不在計數範圍內，須排除。
//
//   1 = 含第一款（計入規則①連3日，及②③④「任一款」）
//   2 = 第二款~第八款（只計入規則②③④「任一款」）
//   0 = 僅第九款~第十四款（不計入任何處置規則）
//
// 注意子字串陷阱：
//   - 「第十一款」.includes('第一款') 為 false（中間隔了「十」），安全。
//   - /第[二三四五六七八]款/ 不會誤中「第十X款」（第二字為「十」）。
//   - /第[九十]/ 可涵蓋 第九、第十、第十一~第十四款。
// 無可辨識款別時保守回傳 2（沿用舊行為，避免漏算）。
export function classifyNoticeLevel(info: string): 0 | 1 | 2 {
  if (info.includes('第一款')) return 1
  if (/第[二三四五六七八]款/.test(info)) return 2
  if (/第[九十]/.test(info)) return 0
  return 2
}
